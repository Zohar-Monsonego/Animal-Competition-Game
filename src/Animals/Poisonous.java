package Animals;


/**
 * The Poisonous enum represents the level of toxicity of a snake venom.
 * It defines several levels of toxicity:
 * - LOW - Represents low toxicity venom
 * - MID - Represents moderate toxicity venom
 * - HIGH - Represents high toxicity venom
 */
public enum Poisonous {
	LOW, MID, HIGH;

}
