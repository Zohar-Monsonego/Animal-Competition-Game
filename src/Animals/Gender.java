package Animals;


/**
 * The Gender enum represents the gender of an animal.
 * It defines three possible values:
 * - MALE - Represents a male animal.
 * - FEMALE - Represents a female animal.
 * - HERMAPHRODITE - Represents an animal that has both male and female reproductive organs.
 */
public enum Gender {
	MALE, FEMALE, HERMAPHRODITE;

}
